# –ò–º–ø–æ—Ä—Ç –Ω–µ–æ–±—Ö–æ–¥–∏–º—ã—Ö –±–∏–±–ª–∏–æ—Ç–µ–∫
import tkinter as tk  # –∏–º–ø–æ—Ä—Ç–∏—Ä—É–µ—Ç –±–∏–±–ª–∏–æ—Ç–µ–∫—É tkinter –¥–ª—è —Å–æ–∑–¥–∞–Ω–∏—è –≥—Ä–∞—Ñ–∏—á–µ—Å–∫–æ–≥–æ –∏–Ω—Ç–µ—Ä—Ñ–µ–π—Å–∞
from tkinter import ttk, scrolledtext  # –∏–º–ø–æ—Ä—Ç–∏—Ä—É–µ—Ç –º–æ–¥—É–ª–∏ –¥–ª—è —Å—Ç–∏–ª–∏–∑–æ–≤–∞–Ω–Ω—ã—Ö –≤–∏–¥–∂–µ—Ç–æ–≤ –∏ —Ç–µ–∫—Å—Ç–æ–≤–æ–≥–æ –ø–æ–ª—è —Å –ø—Ä–æ–∫—Ä—É—Ç–∫–æ–π
import random  # –∏–º–ø–æ—Ä—Ç–∏—Ä—É–µ—Ç –º–æ–¥—É–ª—å random –¥–ª—è –≥–µ–Ω–µ—Ä–∞—Ü–∏–∏ —Å–ª—É—á–∞–π–Ω—ã—Ö —á–∏—Å–µ–ª

# –°–æ–∑–¥–∞–µ–º –≥–ª–∞–≤–Ω–æ–µ –æ–∫–Ω–æ –ø—Ä–∏–ª–æ–∂–µ–Ω–∏—è
root = tk.Tk()  # —Å–æ–∑–¥–∞–µ—Ç –≥–ª–∞–≤–Ω–æ–µ –æ–∫–Ω–æ –ø—Ä–∏–ª–æ–∂–µ–Ω–∏—è
root.title("–î–µ–º–æ–Ω—Å—Ç—Ä–∞—Ü–∏—è –±–∏—Ç–æ–≤—ã—Ö –∏—Å–∫–∞–∂–µ–Ω–∏–π —Ç–µ–∫—Å—Ç–∞ | –†–∞–±–æ—Ç–∞ –ü–∞–≤–ª–∞ –Æ–Ω–∫–µ—Ä")  # —É—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ—Ç –∑–∞–≥–æ–ª–æ–≤–æ–∫ –æ–∫–Ω–∞
root.geometry("1100x800")  # —É—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ—Ç —Ä–∞–∑–º–µ—Ä –æ–∫–Ω–∞ 1100x800 –ø–∏–∫—Å–µ–ª–µ–π
root.configure(bg='#f5f5f5')  # —É—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ—Ç —Å–≤–µ—Ç–ª–æ-—Å–µ—Ä—ã–π —Ü–≤–µ—Ç —Ñ–æ–Ω–∞ –æ–∫–Ω–∞

# –ì–ª–æ–±–∞–ª—å–Ω—ã–µ –ø–µ—Ä–µ–º–µ–Ω–Ω—ã–µ
original_text = tk.StringVar()  # —Å–æ–∑–¥–∞–µ—Ç –ø–µ—Ä–µ–º–µ–Ω–Ω—É—é –¥–ª—è —Ö—Ä–∞–Ω–µ–Ω–∏—è –æ—Ä–∏–≥–∏–Ω–∞–ª—å–Ω–æ–≥–æ —Ç–µ–∫—Å—Ç–∞
error_bits = tk.IntVar(value=1)  # —Å–æ–∑–¥–∞–µ—Ç –ø–µ—Ä–µ–º–µ–Ω–Ω—É—é –¥–ª—è –∫–æ–ª–∏—á–µ—Å—Ç–≤–∞ –∏—Å–∫–∞–∂–∞–µ–º—ã—Ö –±–∏—Ç–æ–≤ —Å –∑–Ω–∞—á–µ–Ω–∏–µ–º –ø–æ —É–º–æ–ª—á–∞–Ω–∏—é 1
error_probability = tk.IntVar(value=10)  # —Å–æ–∑–¥–∞–µ—Ç –ø–µ—Ä–µ–º–µ–Ω–Ω—É—é –¥–ª—è –≤–µ—Ä–æ—è—Ç–Ω–æ—Å—Ç–∏ –æ—à–∏–±–∫–∏ —Å –∑–Ω–∞—á–µ–Ω–∏–µ–º –ø–æ —É–º–æ–ª—á–∞–Ω–∏—é 10%
encoding_type = tk.StringVar(value="utf-8")  # —Å–æ–∑–¥–∞–µ—Ç –ø–µ—Ä–µ–º–µ–Ω–Ω—É—é –¥–ª—è —Ç–∏–ø–∞ –∫–æ–¥–∏—Ä–æ–≤–∫–∏ —Å –∑–Ω–∞—á–µ–Ω–∏–µ–º –ø–æ —É–º–æ–ª—á–∞–Ω–∏—é UTF-8

# –§—É–Ω–∫—Ü–∏—è –¥–ª—è –ø—Ä–∏–º–µ–Ω–µ–Ω–∏—è –±–∏—Ç–æ–≤—ã—Ö –∏—Å–∫–∞–∂–µ–Ω–∏–π –∫ —Ç–µ–∫—Å—Ç—É
def apply_bit_errors(text, num_bits, probability, encoding):
    """
    –§—É–Ω–∫—Ü–∏—è –¥–ª—è –ø—Ä–∏–º–µ–Ω–µ–Ω–∏—è –±–∏—Ç–æ–≤—ã—Ö –∏—Å–∫–∞–∂–µ–Ω–∏–π –∫ —Ç–µ–∫—Å—Ç—É
    """
    try:
        # –ö–æ–¥–∏—Ä—É–µ–º —Ç–µ–∫—Å—Ç –≤ –±–∞–π—Ç—ã
        byte_data = text.encode(encoding)  # –∫–æ–¥–∏—Ä—É–µ—Ç —Ç–µ–∫—Å—Ç –≤ –±–∞–π—Ç—ã –∏—Å–ø–æ–ª—å–∑—É—è –≤—ã–±—Ä–∞–Ω–Ω—É—é –∫–æ–¥–∏—Ä–æ–≤–∫—É
        byte_list = list(byte_data)  # –ø—Ä–µ–æ–±—Ä–∞–∑—É–µ—Ç –±–∞–π—Ç—ã –≤ —Å–ø–∏—Å–æ–∫ –¥–ª—è –∏–∑–º–µ–Ω–µ–Ω–∏—è
        
        result_info = ""  # —Å–æ–∑–¥–∞–µ—Ç –ø—É—Å—Ç—É—é —Å—Ç—Ä–æ–∫—É –¥–ª—è –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏–∏ –æ —Ä–µ–∑—É–ª—å—Ç–∞—Ç–µ
        changed_bytes = 0  # —Å—á–µ—Ç—á–∏–∫ –∏–∑–º–µ–Ω–µ–Ω–Ω—ã—Ö –±–∞–π—Ç–æ–≤
        total_errors = 0  # —Å—á–µ—Ç—á–∏–∫ –æ–±—â–µ–≥–æ –∫–æ–ª–∏—á–µ—Å—Ç–≤–∞ –æ—à–∏–±–æ–∫
        
        # –ü—Ä–æ—Ö–æ–¥–∏–º –ø–æ –≤—Å–µ–º –±–∞–π—Ç–∞–º
        for byte_index in range(len(byte_list)):  # —Ü–∏–∫–ª –ø–æ –∏–Ω–¥–µ–∫—Å–∞–º –±–∞–π—Ç–æ–≤
            original_byte = byte_list[byte_index]  # —Å–æ—Ö—Ä–∞–Ω—è–µ—Ç –æ—Ä–∏–≥–∏–Ω–∞–ª—å–Ω—ã–π –±–∞–π—Ç
            current_byte = original_byte  # —É—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ—Ç —Ç–µ–∫—É—â–∏–π –±–∞–π—Ç –∫–∞–∫ –æ—Ä–∏–≥–∏–Ω–∞–ª—å–Ω—ã–π
            
            # –ü—Ä–∏–º–µ–Ω—è–µ–º –∏—Å–∫–∞–∂–µ–Ω–∏—è —Å –∑–∞–¥–∞–Ω–Ω–æ–π –≤–µ—Ä–æ—è—Ç–Ω–æ—Å—Ç—å—é
            if random.randint(1, 100) <= probability:  # –≥–µ–Ω–µ—Ä–∏—Ä—É–µ—Ç —Å–ª—É—á–∞–π–Ω–æ–µ —á–∏—Å–ª–æ –∏ –ø—Ä–æ–≤–µ—Ä—è–µ—Ç –≤–µ—Ä–æ—è—Ç–Ω–æ—Å—Ç—å
                changed_bytes += 1  # —É–≤–µ–ª–∏—á–∏–≤–∞–µ—Ç —Å—á–µ—Ç—á–∏–∫ –∏–∑–º–µ–Ω–µ–Ω–Ω—ã—Ö –±–∞–π—Ç–æ–≤
                
                # –í—ã–±–∏—Ä–∞–µ–º —Å–ª—É—á–∞–π–Ω—ã–µ –±–∏—Ç—ã –¥–ª—è –∏–Ω–≤–µ—Ä—Å–∏–∏
                bits_to_flip = random.sample(range(8), min(num_bits, 8))  # –≤—ã–±–∏—Ä–∞–µ—Ç —Å–ª—É—á–∞–π–Ω—ã–µ –±–∏—Ç—ã –¥–ª—è –∏–∑–º–µ–Ω–µ–Ω–∏—è
                
                for bit_pos in bits_to_flip:  # —Ü–∏–∫–ª –ø–æ –ø–æ–∑–∏—Ü–∏—è–º –±–∏—Ç–æ–≤ –¥–ª—è –∏–∑–º–µ–Ω–µ–Ω–∏—è
                    # –°–æ–∑–¥–∞–µ–º –º–∞—Å–∫—É –¥–ª—è –∏–Ω–≤–µ—Ä—Å–∏–∏ –±–∏—Ç–∞
                    mask = 1 << bit_pos  # —Å–æ–∑–¥–∞–µ—Ç –±–∏—Ç–æ–≤—É—é –º–∞—Å–∫—É –¥–ª—è –∫–æ–Ω–∫—Ä–µ—Ç–Ω–æ–π –ø–æ–∑–∏—Ü–∏–∏ –±–∏—Ç–∞
                    current_byte ^= mask  # –ø—Ä–∏–º–µ–Ω—è–µ—Ç XOR –¥–ª—è –∏–Ω–≤–µ—Ä—Å–∏–∏ –±–∏—Ç–∞
                    total_errors += 1  # —É–≤–µ–ª–∏—á–∏–≤–∞–µ—Ç —Å—á–µ—Ç—á–∏–∫ –æ—à–∏–±–æ–∫
                
                # –ó–∞–ø–∏—Å—ã–≤–∞–µ–º –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—é –æ–± –∏–∑–º–µ–Ω–µ–Ω–∏–∏
                if byte_index < 10:  # –æ–≥—Ä–∞–Ω–∏—á–∏–≤–∞–µ–º –≤—ã–≤–æ–¥ –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏–∏ –¥–ª—è –ø–µ—Ä–≤—ã—Ö 10 –±–∞–π—Ç–æ–≤
                    result_info += f"–ë–∞–π—Ç {byte_index}: {original_byte:08b} ‚Üí {current_byte:08b}\n"  # –¥–æ–±–∞–≤–ª—è–µ—Ç –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—é –æ–± –∏–∑–º–µ–Ω–µ–Ω–∏–∏ –±–∞–π—Ç–∞
            
            byte_list[byte_index] = current_byte  # –æ–±–Ω–æ–≤–ª—è–µ—Ç –±–∞–π—Ç –≤ —Å–ø–∏—Å–∫–µ
        
        # –ü—Ä–µ–æ–±—Ä–∞–∑—É–µ–º –æ–±—Ä–∞—Ç–Ω–æ –≤ –±–∞–π—Ç—ã –∏ –¥–µ–∫–æ–¥–∏—Ä—É–µ–º
        corrupted_bytes = bytes(byte_list)  # –ø—Ä–µ–æ–±—Ä–∞–∑—É–µ—Ç —Å–ø–∏—Å–æ–∫ –æ–±—Ä–∞—Ç–Ω–æ –≤ –±–∞–π—Ç—ã
        try:
            corrupted_text = corrupted_bytes.decode(encoding)  # –ø—ã—Ç–∞–µ—Ç—Å—è –¥–µ–∫–æ–¥–∏—Ä–æ–≤–∞—Ç—å –∏—Å–∫–∞–∂–µ–Ω–Ω—ã–µ –±–∞–π—Ç—ã
        except UnicodeDecodeError:  # –µ—Å–ª–∏ –≤–æ–∑–Ω–∏–∫–∞–µ—Ç –æ—à–∏–±–∫–∞ –¥–µ–∫–æ–¥–∏—Ä–æ–≤–∞–Ω–∏—è
            # –ü—ã—Ç–∞–µ–º—Å—è –∑–∞–º–µ–Ω–∏—Ç—å –Ω–µ–∫–æ—Ä—Ä–µ–∫—Ç–Ω—ã–µ –±–∞–π—Ç—ã
            corrupted_text = corrupted_bytes.decode(encoding, errors='replace')  # –¥–µ–∫–æ–¥–∏—Ä—É–µ—Ç —Å –∑–∞–º–µ–Ω–æ–π –Ω–µ–∫–æ—Ä—Ä–µ–∫—Ç–Ω—ã—Ö —Å–∏–º–≤–æ–ª–æ–≤
        
        # –§–æ—Ä–º–∏—Ä—É–µ–º –ø–æ–ª–Ω—É—é –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—é
        info = f"–ò–°–•–û–î–ù–´–ô –¢–ï–ö–°–¢ ({len(text)} —Å–∏–º–≤–æ–ª–æ–≤, {len(byte_data)} –±–∞–π—Ç):\n"  # –Ω–∞—á–∏–Ω–∞–µ—Ç —Ñ–æ—Ä–º–∏—Ä–æ–≤–∞—Ç—å –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—é
        info += f"'{text}'\n\n"  # –¥–æ–±–∞–≤–ª—è–µ—Ç –æ—Ä–∏–≥–∏–Ω–∞–ª—å–Ω—ã–π —Ç–µ–∫—Å—Ç
        info += f"–ò–°–ö–ê–ñ–ï–ù–ù–´–ô –¢–ï–ö–°–¢:\n"  # –¥–æ–±–∞–≤–ª—è–µ—Ç –∑–∞–≥–æ–ª–æ–≤–æ–∫ –¥–ª—è –∏—Å–∫–∞–∂–µ–Ω–Ω–æ–≥–æ —Ç–µ–∫—Å—Ç–∞
        info += f"'{corrupted_text}'\n\n"  # –¥–æ–±–∞–≤–ª—è–µ—Ç –∏—Å–∫–∞–∂–µ–Ω–Ω—ã–π —Ç–µ–∫—Å—Ç
        info += "–°–¢–ê–¢–ò–°–¢–ò–ö–ê –ò–°–ö–ê–ñ–ï–ù–ò–ô:\n"  # –¥–æ–±–∞–≤–ª—è–µ—Ç –∑–∞–≥–æ–ª–æ–≤–æ–∫ –¥–ª—è —Å—Ç–∞—Ç–∏—Å—Ç–∏–∫–∏
        info += f"‚Ä¢ –ò–∑–º–µ–Ω–µ–Ω–æ –±–∞–π—Ç–æ–≤: {changed_bytes} –∏–∑ {len(byte_list)} ({changed_bytes/len(byte_list)*100:.1f}%)\n"  # –¥–æ–±–∞–≤–ª—è–µ—Ç —Å—Ç–∞—Ç–∏—Å—Ç–∏–∫—É –ø–æ –±–∞–π—Ç–∞–º
        info += f"‚Ä¢ –í—Å–µ–≥–æ –∏–Ω–≤–µ—Ä—Å–∏–π –±–∏—Ç–æ–≤: {total_errors}\n"  # –¥–æ–±–∞–≤–ª—è–µ—Ç –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –∏–Ω–≤–µ—Ä—Å–∏–π –±–∏—Ç–æ–≤
        info += f"‚Ä¢ –í–µ—Ä–æ—è—Ç–Ω–æ—Å—Ç—å –æ—à–∏–±–∫–∏: {probability}%\n"  # –¥–æ–±–∞–≤–ª—è–µ—Ç –≤–µ—Ä–æ—è—Ç–Ω–æ—Å—Ç—å –æ—à–∏–±–∫–∏
        info += f"‚Ä¢ –ë–∏—Ç–æ–≤ –Ω–∞ –æ—à–∏–±–∫—É: {num_bits}\n\n"  # –¥–æ–±–∞–≤–ª—è–µ—Ç –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –±–∏—Ç–æ–≤ –Ω–∞ –æ—à–∏–±–∫—É
        info += "–ü–†–ò–ú–ï–†–´ –ò–ó–ú–ï–ù–ï–ù–ò–ô –ë–ê–ô–¢–û–í:\n"  # –¥–æ–±–∞–≤–ª—è–µ—Ç –∑–∞–≥–æ–ª–æ–≤–æ–∫ –¥–ª—è –ø—Ä–∏–º–µ—Ä–æ–≤ –∏–∑–º–µ–Ω–µ–Ω–∏–π
        info += result_info  # –¥–æ–±–∞–≤–ª—è–µ—Ç –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—é –æ–± –∏–∑–º–µ–Ω–µ–Ω–∏—è—Ö –±–∞–π—Ç–æ–≤
        
        return corrupted_text, info  # –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç –∏—Å–∫–∞–∂–µ–Ω–Ω—ã–π —Ç–µ–∫—Å—Ç –∏ –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—é
    
    except Exception as e:  # –µ—Å–ª–∏ –≤–æ–∑–Ω–∏–∫–∞–µ—Ç –ª—é–±–∞—è –æ—à–∏–±–∫–∞
        return f"–û—à–∏–±–∫–∞: {str(e)}", f"–ü—Ä–æ–∏–∑–æ—à–ª–∞ –æ—à–∏–±–∫–∞ –ø—Ä–∏ –æ–±—Ä–∞–±–æ—Ç–∫–µ —Ç–µ–∫—Å—Ç–∞: {str(e)}"  # –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç —Å–æ–æ–±—â–µ–Ω–∏–µ –æ–± –æ—à–∏–±–∫–µ

# –§—É–Ω–∫—Ü–∏—è –¥–ª—è –¥–µ–º–æ–Ω—Å—Ç—Ä–∞—Ü–∏–∏ —Ä–∞–∑–ª–∏—á–Ω—ã—Ö —Ç–∏–ø–æ–≤ –∏—Å–∫–∞–∂–µ–Ω–∏–π
def demonstrate_error_types():
    """
    –§—É–Ω–∫—Ü–∏—è –¥–ª—è –¥–µ–º–æ–Ω—Å—Ç—Ä–∞—Ü–∏–∏ —Ä–∞–∑–ª–∏—á–Ω—ã—Ö —Ç–∏–ø–æ–≤ –±–∏—Ç–æ–≤—ã—Ö –∏—Å–∫–∞–∂–µ–Ω–∏–π
    """
    text = original_text.get()  # –ø–æ–ª—É—á–∞–µ—Ç —Ç–µ–∫—Å—Ç –∏–∑ –ø–µ—Ä–µ–º–µ–Ω–Ω–æ–π
    if not text.strip():  # –ø—Ä–æ–≤–µ—Ä—è–µ—Ç, —á—Ç–æ —Ç–µ–∫—Å—Ç –Ω–µ –ø—É—Å—Ç–æ–π
        return  # –≤—ã—Ö–æ–¥–∏—Ç –∏–∑ —Ñ—É–Ω–∫—Ü–∏–∏ –µ—Å–ª–∏ —Ç–µ–∫—Å—Ç –ø—É—Å—Ç–æ–π
    
    encoding = encoding_type.get()  # –ø–æ–ª—É—á–∞–µ—Ç –≤—ã–±—Ä–∞–Ω–Ω—É—é –∫–æ–¥–∏—Ä–æ–≤–∫—É
    
    # –°–æ–∑–¥–∞–µ–º —Ñ—Ä–µ–π–º –¥–ª—è –æ—Ç–æ–±—Ä–∞–∂–µ–Ω–∏—è —Ä–∞–∑–ª–∏—á–Ω—ã—Ö —Å—Ü–µ–Ω–∞—Ä–∏–µ–≤
    results = ""  # —Å–æ–∑–¥–∞–µ—Ç –ø—É—Å—Ç—É—é —Å—Ç—Ä–æ–∫—É –¥–ª—è —Ä–µ–∑—É–ª—å—Ç–∞—Ç–æ–≤
    results += "–î–ï–ú–û–ù–°–¢–†–ê–¶–ò–Ø –†–ê–ó–õ–ò–ß–ù–´–• –¢–ò–ü–û–í –ò–°–ö–ê–ñ–ï–ù–ò–ô\n"  # –¥–æ–±–∞–≤–ª—è–µ—Ç –∑–∞–≥–æ–ª–æ–≤–æ–∫
    results += "=" * 60 + "\n\n"  # –¥–æ–±–∞–≤–ª—è–µ—Ç —Ä–∞–∑–¥–µ–ª–∏—Ç–µ–ª—å–Ω—É—é –ª–∏–Ω–∏—é
    
    # –°—Ü–µ–Ω–∞—Ä–∏–π 1: –û–¥–∏–Ω–æ—á–Ω—ã–µ –±–∏—Ç–æ–≤—ã–µ –æ—à–∏–±–∫–∏ (–Ω–∏–∑–∫–∞—è –≤–µ—Ä–æ—è—Ç–Ω–æ—Å—Ç—å)
    corrupted1, info1 = apply_bit_errors(text, 1, 5, encoding)  # –ø—Ä–∏–º–µ–Ω—è–µ—Ç –æ–¥–∏–Ω–æ—á–Ω—ã–µ –±–∏—Ç–æ–≤—ã–µ –æ—à–∏–±–∫–∏ —Å –Ω–∏–∑–∫–æ–π –≤–µ—Ä–æ—è—Ç–Ω–æ—Å—Ç—å—é
    results += "1. –û–î–ò–ù–û–ß–ù–´–ï –ë–ò–¢–û–í–´–ï –û–®–ò–ë–ö–ò (1 –±–∏—Ç, 5% –≤–µ—Ä–æ—è—Ç–Ω–æ—Å—Ç—å):\n"  # –¥–æ–±–∞–≤–ª—è–µ—Ç –æ–ø–∏—Å–∞–Ω–∏–µ —Å—Ü–µ–Ω–∞—Ä–∏—è
    results += f"   –†–µ–∑—É–ª—å—Ç–∞—Ç: {corrupted1}\n\n"  # –¥–æ–±–∞–≤–ª—è–µ—Ç —Ä–µ–∑—É–ª—å—Ç–∞—Ç
    
    # –°—Ü–µ–Ω–∞—Ä–∏–π 2: –ú–Ω–æ–∂–µ—Å—Ç–≤–µ–Ω–Ω—ã–µ –±–∏—Ç–æ–≤—ã–µ –æ—à–∏–±–∫–∏
    corrupted2, info2 = apply_bit_errors(text, 2, 15, encoding)  # –ø—Ä–∏–º–µ–Ω—è–µ—Ç –º–Ω–æ–∂–µ—Å—Ç–≤–µ–Ω–Ω—ã–µ –±–∏—Ç–æ–≤—ã–µ –æ—à–∏–±–∫–∏
