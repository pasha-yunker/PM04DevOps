        decoded_text = bytes_data.decode(encoding)  # –¥–µ–∫–æ–¥–∏—Ä—É–µ—Ç –±–∞–π—Ç—ã –≤ —Ç–µ–∫—Å—Ç –∏—Å–ø–æ–ª—å–∑—É—è –≤—ã–±—Ä–∞–Ω–Ω—É—é –∫–æ–¥–∏—Ä–æ–≤–∫—É
        
