        result += f"Base64: {base64_string}\n\n"  # –¥–æ–±–∞–≤–ª—è–µ—Ç Base64 —Å—Ç—Ä–æ–∫—É
        
        # –î–æ–ø–æ–ª–Ω–∏—Ç–µ–ª—å–Ω–∞—è –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—è
